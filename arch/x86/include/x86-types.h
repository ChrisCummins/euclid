/*
 * Typedefs for use in the kernel.
 */

#ifndef _x86_TYPES_H
#define _x86_TYPES_H

/* Integer sizes */
typedef unsigned char   u8;
typedef          char   s8;
typedef unsigned short  u16;
typedef          short  s16;
typedef unsigned int    u32;
typedef          int    s32;

/* Port */
typedef u16 port_t;

#endif /* _x86_TYPES_H */
